#include "ft_printf.h"

void	ft_putnbr(long n)
{
	long long	nb;

	nb = n;
	if (nb < 0)
	{
		write(1, "-", 1);
		nb = -nb;
	}
	if (nb >= 10)
		ft_putnbr(nb / 10);
	ft_putchar_fd(nb % 10 + '0', 1);
}
